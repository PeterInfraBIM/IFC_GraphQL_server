package nl.infrabim.ifc.dataserver.models;

public class IfcMaterial {
	private String type;
	private String name;

	public IfcMaterial() {
		super();
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
